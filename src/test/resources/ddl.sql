CREATE TABLE STUDY_MEETING (
    ID INTEGER PRIMARY KEY,
    TITLE VARCHAR(128) NOT NULL,
    SUB_TITLE VARCHAR(128) NOT NULL,
    CONTEXT VARCHAR(1024) NOT NULL,
    EXHIBITION_DATE DATE NOT NULL,
    PARTICIPANT_COUNT INTEGER 
);

