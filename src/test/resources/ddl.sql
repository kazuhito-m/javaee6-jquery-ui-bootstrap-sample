COMMENT ON COLUMN STUDY_MEETING.CONTEXT IS '内容';
COMMENT ON COLUMN STUDY_MEETING.EXHIBITION_DATE IS '開催日';
COMMENT ON COLUMN STUDY_MEETING.PARTICIPANT_COUNT IS '参加者数';

